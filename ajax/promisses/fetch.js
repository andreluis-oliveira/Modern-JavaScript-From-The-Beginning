function createPost(post) {
  return new Promise((resolve, reject) => {
    setTimeout(() => {
      posts.push(post);
      const error = false;

      if (!error) {
        resolve();
      } else {
        reject('Error: Something went wrong');
      }

      resolve();
    }, 2000);
  });
}

function getPosts() {
  setTimeout(() => {
    let output = '<ul>';

    posts.forEach((post) => {
      output += `<li>${post.title}</li>`;
    });

    output += '</ul>';

    document.getElementById('posts').innerHTML = output;
  }, 1000);
}

